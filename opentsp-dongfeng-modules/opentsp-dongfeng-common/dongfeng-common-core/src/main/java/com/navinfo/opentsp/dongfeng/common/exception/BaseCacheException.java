package com.navinfo.opentsp.dongfeng.common.exception;

public class BaseCacheException extends BaseException {

	/**
	 * 
	 */
	private static final long serialVersionUID = 1L;
	
	public BaseCacheException() {
		super();
	}
	
	public BaseCacheException(String message) {
		super(message);
	}
	
	public BaseCacheException(Throwable cause) {
		super(cause);
	}
	
	public BaseCacheException(String message , Throwable cause) {
		super(message , cause);
	}

}
